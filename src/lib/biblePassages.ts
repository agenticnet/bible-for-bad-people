export function searchPassages(
  query: string,
  activeTags: Set<string>
): BiblePassage[] {
  const q = query.trim().toLowerCase();

  return BIBLE_PASSAGES.filter((passage) => {
    const matchesTags =
      activeTags.size === 0 ||
      passage.tags.some((tag) => activeTags.has(tag));

    if (!matchesTags) return false;
    if (!q) return true;

    const haystack = [
      passage.reference,
      passage.book,
      passage.excerpt,
      passage.tldr,
      passage.modernWorld,
      ...passage.tags,
    ]
      .join(" ")
      .toLowerCase();

    return haystack.includes(q);
  });
}

export function getPassageById(id: string): BiblePassage | undefined {
  return BIBLE_PASSAGES.find((p) => p.id === id);
}
