function hashDate(dateKey: string): number {
  let hash = 0;
  for (let i = 0; i < dateKey.length; i++) {
    hash = (hash << 5) - hash + dateKey.charCodeAt(i);
    hash |= 0;
  }
  return Math.abs(hash);
}

export function getDateKey(date = new Date()): string {
  return date.toISOString().slice(0, 10);
}

export function getDailySins(dateKey: string, count = 7): SinEntry[] {
  const hash = hashDate(dateKey);
  const pool = [...SIN_LIBRARY];
  const picked: SinEntry[] = [];

  for (let i = 0; i < count && pool.length > 0; i++) {
    const index = (hash + i * 13) % pool.length;
    picked.push(pool[index]);
    pool.splice(index, 1);
  }

  return picked;
}

export function searchSins(query: string, category?: string): SinEntry[] {
  const q = query.trim().toLowerCase();
  return SIN_LIBRARY.filter((sin) => {
    if (category && sin.category !== category) return false;
    if (!q) return true;
    const haystack = [sin.petty, sin.translation, sin.category].join(" ").toLowerCase();
    return haystack.includes(q);
  });
}

export function getSinById(id: string): SinEntry | undefined {
  return SIN_LIBRARY.find((s) => s.id === id);
}

